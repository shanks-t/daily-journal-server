class Entry():

    # Class initializer. It has 5 custom parameters, with the
    # special `self` parameter that every method on a class
    # needs as the first parameter.
    def __init__(self, id, concept, entry, mood_id, date):
        self.id = id
        self.entry = entry
        self.concept = concept
        self.entry = entry
        self.moodId = mood_id
        self.date = date
